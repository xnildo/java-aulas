package metodosStrings;

public class Math_Metodos {

	public static void main(String[] args) {
		int n1 = 10, n2 = 20;
		
		//metodo Math.max() retorna o maior valor 
		System.out.println("Maior valor: " +Math.max(n1, n2));

		//metodo Math.min() retorna o menor valor 
		System.out.println("Menor valor: " +Math.min(n1, n2));
		
		//O método retorna a raiz quadrada de x :Math.sqrt(x)
		System.out.println("Raiz de 64: " +Math.sqrt(64));
		
		//O método retorna o valor absoluto (positivo) de x :Math.abs(x)
		System.out.println("Valor Absoluto: " +Math.abs(-45.5));
	}

}
